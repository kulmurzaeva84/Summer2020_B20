package day12_Switch_Scanner;

public class Switch_Practice2 {
    public static void main(String[] args) {

        String productName = "iPhone";

        switch (productName) {

            case "galaxy":
                System.out.println("Samsung");
                break;
            case "iPhone":
            case "MacBook":
            case "iPad":
                System.out.println("Apple");
                break;




        }

    }







}
